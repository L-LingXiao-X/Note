package class1.summer_camping;


public class SwitchDemo1 {
    public static void main(String[] args) {
        //TODO: konw how to use switch

        String weekday = "Wednesday";
        switch (weekday){
            case "Monday":
                System.out.println("work hard");
                break;

            case "Tuesday":
                System.out.println("ask for help");
                break;

            case "Wednesday":
                System.out.println("happy");
                break;

            case "Thursday":
                System.out.println("help each other");
                break;

            case "Friday":
                System.out.println("walk");
                break;

            default:
                System.out.println("data has some problems");
       }
   }
}
